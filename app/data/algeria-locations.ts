export type AlgeriaWilayaName = keyof typeof ALGERIA_WILAYA_DATA;

export type AlgeriaWilayaData =
  (typeof ALGERIA_WILAYA_DATA)[AlgeriaWilayaName];

export function normalizeAlgeriaLocationName(value?: string) {
  return (value || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-zA-Z0-9]+/g, " ")
    .trim()
    .replace(/\s+/g, " ")
    .toLowerCase();
}

const NORMALIZED_WILAYA_LOOKUP = Object.fromEntries(
  (Object.keys(ALGERIA_WILAYA_DATA) as AlgeriaWilayaName[]).map((wilaya) => [
    normalizeAlgeriaLocationName(wilaya),
    wilaya,
  ]),
) as Record<string, AlgeriaWilayaName>;

export function getWilayaName(value?: string): AlgeriaWilayaName | null {
  const key = normalizeAlgeriaLocationName(value);

  if (!key) {
    return null;
  }

  return NORMALIZED_WILAYA_LOOKUP[key] ?? null;
}

export function getWilayaData(value?: string): AlgeriaWilayaData | null {
  const wilayaName = getWilayaName(value);

  if (!wilayaName) {
    return null;
  }

  return ALGERIA_WILAYA_DATA[wilayaName];
}

export function isCommuneInWilaya(wilaya?: string, commune?: string) {
  const wilayaData = getWilayaData(wilaya);
  const normalizedCommune = normalizeAlgeriaLocationName(commune);

  if (!wilayaData || !normalizedCommune) {
    return false;
  }

  return wilayaData.communes.some(
    (item) => normalizeAlgeriaLocationName(item) === normalizedCommune,
  );
}
